#Bitwise operators
x=7
y=13
print(x&y)#(AND)
print(x|y)#(OR)
print(x^y)#(XOR)
print(~x)#(NEGATION)
print(x<<2)#(RIGHT SHIFT)
print(x>>2)#(LEFT SHIFT)
